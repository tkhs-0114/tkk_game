CREATE TABLE koma(
  id INT PRIMARY KEY AUTO_INCREMENT,
  name VARCHAR(50) NOT NULL
);

CREATE TABLE Rule(
  id INT PRIMARY KEY AUTO_INCREMENT,
  text VARCHAR(50) NOT NULL
);

CREATE TABLE KomaRule(
  id INT PRIMARY KEY AUTO_INCREMENT,
  koma_id INT,
  rule_id INT,
  FOREIGN KEY (koma_id) REFERENCES koma(id),
  FOREIGN KEY (rule_id) REFERENCES Rule(id)
);

CREATE TABLE Deck(
  id INT PRIMARY KEY AUTO_INCREMENT,
  name VARCHAR(255),
  sfen VARCHAR(255)
);
